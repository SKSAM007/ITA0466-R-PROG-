v1 <- c(1, 2, 3, 4, 5, 6, 7, 8, 9)
v2 <- c(9, 8, 7, 6, 5, 4, 3, 2, 1)
m1 <- matrix(v1, nrow = 3, ncol = 3, byrow = TRUE)
m2 <- matrix(v2, nrow = 3, ncol = 3, byrow = TRUE)
arr <- array(c(m1, m2), dim = c(3, 3, 2))
print("Second row of the second matrix:")
print(arr[2, , 2])
print("Element in the 3rd row and 3rd column of the 1st matrix:")
print(arr[3, 3, 1])
